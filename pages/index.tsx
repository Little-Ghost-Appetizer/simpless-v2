import Head from "next/head";
import { ChangeEvent, useState } from "react";
import BadgeList from "../components/BadgeList";
import { Icon } from '@iconify/react';


export default function Home() {
  const [tweet, setTweet] = useState('')

  function handleChange(e: ChangeEvent<HTMLTextAreaElement>){
    setTweet(e.target.value.substring(0, 280))
  }

  return (
    <div className="h-screen bg-slate-200">
      <Head>
        <title>SimplesS</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex h-full">
        <div className="shadow-md bg-white rounded-2xl m-12 p-6 w-3/5 h-2/3 flex-col">
          <textarea className="w-full mb-4 p-4 h-1/2" value={tweet} onChange={handleChange}/>
          <div className="flex"> 
            <div className="flex-1">{`${tweet.length} / 280`}</div> 
            <button className="bg-slate-400 rounded-full p-2 hover:bg-slate-600">
              <Icon icon="akar-icons:copy" className="text-xl text-white"/>
            </button>
            
          </div>
        </div>
        <div className="bg-gray-400 flex-col flex-1 p-4">
          <h2 className="text-4xl m-4"> Tags / Keywords </h2>
          <BadgeList texts={["test1", "test2", "test3"]} />
        </div> 
      </main>
    </div>
  );
}
